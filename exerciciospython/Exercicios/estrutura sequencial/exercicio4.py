# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

b1 = int(input ('Qual sua nota do primeiro bimestre? '))
b2 = int(input ('Qual sua nota do segundo bimestre? ' ))
b3 = int(input ('Qual sua nota do terceiro bimestre? ' ))
b4 = int(input ('Qual sua nota do quarto bimestre? ' ))
soma = b1 + b2 + b3 + b4
média = soma / 4
print (f'a média das notas é: {média}')

